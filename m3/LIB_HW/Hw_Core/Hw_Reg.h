#include "Hw.h"

#define     __I     volatile                  /*!< defines 'read only' permissions      */
#define     __O     volatile                  /*!< defines 'write only' permissions     */
#define     __IO    volatile                  /*!< defines 'read / write' permissions   */


typedef enum { 
    RESET = 0, SET   = !RESET 
} FlagStatus, ITStatus;

typedef enum {
    DISABLE = 0, ENABLE  = !DISABLE
} FunctionalState;


